const consentState = {
  fonts: false,
  maps: false
};

function syncConsentInputs() {
  if (consentFontsCheckbox) consentFontsCheckbox.checked = consentState.fonts;
  if (consentMapsCheckbox) consentMapsCheckbox.checked = consentState.maps;
}

function applyConsentSelection(selection) {
  consentState.fonts = Boolean(selection.fonts);
  consentState.maps = Boolean(selection.maps);

  if (consentState.fonts) {
    loadGoogleFonts();
  } else {
    unloadGoogleFonts();
  }

  if (consentState.maps) {
    injectMap();
  } else {
    removeMap();
    showMapPlaceholder();
  }
}

/* Cookie banner visibility */
function lockBodyScroll() {
  document.body.style.position = "fixed";
  document.body.style.top = "0";
  document.body.style.left = "0";
  document.body.style.right = "0";
  document.body.style.overflow = "hidden";
}

function unlockBodyScroll() {
  document.body.style.position = "";
  document.body.style.top = "";
  document.body.style.left = "";
  document.body.style.right = "";
  document.body.style.overflow = "";
}

function showCookieBanner() {
  const el = document.getElementById("cookie-banner");
  syncConsentInputs();
  lockBodyScroll();
  if (el) el.removeAttribute("hidden");
}

function showCookieBannerFor(service) {
  showCookieBanner();
  if (service === "maps" && consentMapsCheckbox) {
    consentMapsCheckbox.scrollIntoView({ block: "center", behavior: "smooth" });
  }
}

function hideCookieBanner() {
  const el = document.getElementById("cookie-banner");
  if (el) el.setAttribute("hidden", "");
  unlockBodyScroll();
}

function initCookieConsent() {
  showMapPlaceholder();

  requestAnimationFrame(() => {
    showCookieBanner();
  });

  document.getElementById("cookie-save")?.addEventListener("click", () => {
    const selection = {
      fonts: consentFontsCheckbox?.checked,
      maps: consentMapsCheckbox?.checked
    };
    applyConsentSelection(selection);
    hideCookieBanner();
  });

  document.getElementById("cookie-accept")?.addEventListener("click", () => {
    if (consentFontsCheckbox) consentFontsCheckbox.checked = true;
    if (consentMapsCheckbox) consentMapsCheckbox.checked = true;
    const selection = { fonts: true, maps: true };
    applyConsentSelection(selection);
    hideCookieBanner();
  });

  document.getElementById("cookie-decline")?.addEventListener("click", () => {
    if (consentFontsCheckbox) consentFontsCheckbox.checked = false;
    if (consentMapsCheckbox) consentMapsCheckbox.checked = false;
    const selection = { fonts: false, maps: false };
    applyConsentSelection(selection);
    hideCookieBanner();
  });
}

/* ═══════════════════════════════════════════════════════════════════════════════
   MODALS (Impressum / Datenschutz)
═══════════════════════════════════════════════════════════════════════════════ */
function openModal(id) {
  const el = document.getElementById(id);
  if (!el) return;
  el.removeAttribute("hidden");
  document.body.style.overflow = "hidden";
}

function closeModal(id) {
  const el = document.getElementById(id);
  if (!el) return;
  el.setAttribute("hidden", "");
  document.body.style.overflow = "";
}

function initModals() {
  /* Impressum */
  document.getElementById("open-impressum")?.addEventListener("click", () => openModal("impressum-overlay"));
  document.getElementById("close-impressum")?.addEventListener("click", () => closeModal("impressum-overlay"));
  document.getElementById("impressum-overlay")?.addEventListener("click", (e) => {
    if (e.target === e.currentTarget) closeModal("impressum-overlay");
  });

  /* Datenschutz */
  document.getElementById("open-datenschutz")?.addEventListener("click", () => openModal("datenschutz-overlay"));
  document.getElementById("open-datenschutz-cookie")?.addEventListener("click", () => openModal("datenschutz-overlay"));
  document.getElementById("close-datenschutz")?.addEventListener("click", () => closeModal("datenschutz-overlay"));
  document.getElementById("datenschutz-overlay")?.addEventListener("click", (e) => {
    if (e.target === e.currentTarget) closeModal("datenschutz-overlay");
  });

  /* ESC key closes any open modal */
  document.addEventListener("keydown", (e) => {
    if (e.key === "Escape") {
      closeModal("impressum-overlay");
      closeModal("datenschutz-overlay");
    }
  });
}

