/* Load Google Fonts dynamically */
function loadGoogleFonts() {
  if (document.getElementById("gf-link")) return;
  const link   = document.createElement("link");
  link.id      = "gf-link";
  link.rel     = "stylesheet";
  link.href    = "https://fonts.googleapis.com/css2?family=Barlow+Condensed:wght@600;700;800&family=Inter:wght@400;500;600;700&display=swap";
  document.head.appendChild(link);
  document.documentElement.classList.add("gf"); // triggers CSS var override
}

function unloadGoogleFonts() {
  document.getElementById("gf-link")?.remove();
  document.documentElement.classList.remove("gf");
}

/* Inject Google Maps iframe */
function injectMap() {
  const wrap = document.getElementById("footer-map");
  if (!wrap || wrap.querySelector("iframe")) return;
  wrap.innerHTML = "";
  const iframe = document.createElement("iframe");
  iframe.title             = "Butler Profi Reifenmontage Karte";
  iframe.loading           = "lazy";
  iframe.src               = "https://www.google.com/maps?q=Draisstra%C3%9Fe+19E,+76461+Muggensturm&output=embed";
  iframe.referrerPolicy    = "no-referrer-when-downgrade";
  iframe.style.cssText     = "width:100%;height:100%;border:0;display:block;filter:grayscale(30%) contrast(1.1)";
  wrap.appendChild(iframe);
}

function removeMap() {
  const wrap = document.getElementById("footer-map");
  if (!wrap) return;
  wrap.querySelector("iframe")?.remove();
}

