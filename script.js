let current = 0;
let intervalId;

function renderCarousel() {
  carouselTrack.innerHTML = slides.map((slide) => `
    <article class="slide">
      <img src="./${slide.folder || "assets"}/${slide.image}" alt="${slide.title}" loading="eager" />
      <div class="slide-caption">
        <strong>${slide.title}</strong>
        <span>${slide.text}</span>
      </div>
    </article>
  `).join("");

  carouselDots.innerHTML = slides.map((_, i) =>
    `<button class="dot ${i === 0 ? "is-active" : ""}" type="button" data-index="${i}" aria-label="Slide ${i + 1}"></button>`
  ).join("");

  carouselDots.querySelectorAll(".dot").forEach((dot) => {
    dot.addEventListener("click", () => { setSlide(Number(dot.dataset.index)); restartAuto(); });
  });
}

function setSlide(index) {
  current = (index + slides.length) % slides.length;
  carouselTrack.style.transform = `translateX(-${current * 100}%)`;
  carouselDots.querySelectorAll(".dot").forEach((dot, i) => {
    dot.classList.toggle("is-active", i === current);
  });
}

function startAuto()   { intervalId = window.setInterval(() => setSlide(current + 1), 4200); }
function restartAuto() { window.clearInterval(intervalId); startAuto(); }

prevButton.addEventListener("click", () => { setSlide(current - 1); restartAuto(); });
nextButton.addEventListener("click", () => { setSlide(current + 1); restartAuto(); });

/* ═══════════════════════════════════════════════════════════════════════════════
   GALLERY SLIDER
═══════════════════════════════════════════════════════════════════════════════ */
let galleryPos = 0; // current index (leftmost visible slide)

function forceInitialScrollTop() {
  if (window.location.hash) return;
  if ("scrollRestoration" in history) {
    history.scrollRestoration = "manual";
  }
  document.documentElement.scrollTop = 0;
  document.body.scrollTop = 0;
  window.scrollTo(0, 0);
  requestAnimationFrame(() => window.scrollTo(0, 0));
  window.setTimeout(() => window.scrollTo(0, 0), 0);
}

function getPerPage() {
  if (window.innerWidth <= 480) return 1;
  if (window.innerWidth <= 900) return 2;
  return 3;
}

function getSlideWidth() {
  const first = gallerySlider.querySelector(".gallery-slide");
  if (!first) return 0;
  return first.offsetWidth + 14; // 14 = gap
}

function updateGalleryCounter() {
  const perPage = getPerPage();
  const from = galleryPos + 1;
  const to   = Math.min(galleryPos + perPage, galleryImages.length);
  galleryCounter.textContent = perPage === 1
    ? `${from} / ${galleryImages.length}`
    : `${from} – ${to} / ${galleryImages.length}`;
}

function updateGalleryButtons() {
  const perPage = getPerPage();
  galleryPrevBtn.disabled = galleryPos === 0;
  galleryNextBtn.disabled = galleryPos >= galleryImages.length - perPage;
}

function scrollGalleryTo(index) {
  const perPage  = getPerPage();
  const maxIndex = Math.max(0, galleryImages.length - perPage);
  galleryPos     = Math.max(0, Math.min(index, maxIndex));
  gallerySlider.scrollLeft = galleryPos * getSlideWidth();
  updateGalleryCounter();
  updateGalleryButtons();
}

function renderGallery() {
  gallerySlider.innerHTML = galleryImages.map((item) => `
    <div class="gallery-slide">
      <img src="./${item.folder || "assets"}/${item.src}" alt="${item.alt}" loading="lazy" decoding="async" />
    </div>
  `).join("");

  updateGalleryCounter();
  updateGalleryButtons();
}

galleryPrevBtn.addEventListener("click", () => scrollGalleryTo(galleryPos - getPerPage()));
galleryNextBtn.addEventListener("click", () => scrollGalleryTo(galleryPos + getPerPage()));

/* Sync counter/buttons when user manually scrolls (touch drag etc.) */
gallerySlider.addEventListener("scroll", () => {
  const sw = getSlideWidth();
  if (sw > 0) galleryPos = Math.round(gallerySlider.scrollLeft / sw);
  updateGalleryCounter();
  updateGalleryButtons();
}, { passive: true });

/* Mouse drag support */
let isDragging = false;
let dragStartX = 0;
let dragStartScroll = 0;

gallerySlider.addEventListener("mousedown", (e) => {
  isDragging    = true;
  dragStartX    = e.clientX;
  dragStartScroll = gallerySlider.scrollLeft;
  gallerySlider.classList.add("is-dragging");
});

window.addEventListener("mousemove", (e) => {
  if (!isDragging) return;
  gallerySlider.scrollLeft = dragStartScroll - (e.clientX - dragStartX);
});

window.addEventListener("mouseup", () => {
  if (!isDragging) return;
  isDragging = false;
  gallerySlider.classList.remove("is-dragging");
  const sw = getSlideWidth();
  if (sw > 0) galleryPos = Math.round(gallerySlider.scrollLeft / sw);
  scrollGalleryTo(galleryPos); // snap to nearest
});

/* Re-align on resize */
let resizeTimer;
window.addEventListener("resize", () => {
  clearTimeout(resizeTimer);
  resizeTimer = setTimeout(() => {
    scrollGalleryTo(galleryPos);
  }, 120);
});

